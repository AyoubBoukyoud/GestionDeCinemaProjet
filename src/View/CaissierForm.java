    private void cmbFilmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbFilmActionPerformed
        // TODO add your handling code here:
        String film = (String)cmbFilm.getSelectedItem();
            if (film != null) {
                try {
                    List<String> shows = controller.getShowtimesFor(film);
                    cmbHeure.removeAllItems();
                    for (String s : shows) cmbHeure.addItem(s);
                } catch (Exception ex) {
                    ex.printStackTrace();
                }
            }
    }//GEN-LAST:event_cmbFilmActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
       try {
                    // load films
                    
                    List<String> movies = controller.getAllMovies();
                    cmbFilm.removeAllItems();
                    for (String m : movies) cmbFilm.addItem(m);

                    // load seat types
                    cmbSType.removeAllItems();
                    for (String t : controller.getSeatTypes())
                        cmbSType.addItem(t);

                } catch (Exception ex) {
                    ex.printStackTrace();
                }
    }//GEN-LAST:event_formWindowOpened

