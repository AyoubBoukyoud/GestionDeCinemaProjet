    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
          try {
                String showInfo = cmbHeure.getSelectedItem().toString();
                int showtimeId = Integer.parseInt(showInfo.split(" ")[2]);
                String customer = txtClientName.getText();
                String seat     = txtSelectedSeat.getText();
                String type     = (String)cmbSType.getSelectedItem();

                boolean ok = controller.bookTicket(showtimeId, customer, seat, type);
                if (!ok) {
                    JOptionPane.showMessageDialog(this, "Booking failed!");
                    return;
                }
                JOptionPane.showMessageDialog(this, "Ticket booked successfully!");

                // Build & print
                double price = type.equals("Premium") ? 15.0 : 10.0;
                String ticketText = controller.buildTicketText(
                    (String)cmbFilm.getSelectedItem(),
                    showInfo, customer, type, seat, price
                );
                controller.printTicket(ticketText);

            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
            }
    }//GEN-LAST:event_jButton2ActionPerformed

